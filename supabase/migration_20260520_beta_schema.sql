-- ============================================================
-- 3. daily_summaries — new table
-- ============================================================
-- Distinct from ai_summaries (which is an older, simpler table).
-- One row per child per calendar day; generated by AI server-side.

CREATE TABLE IF NOT EXISTS daily_summaries (
  id           TEXT        PRIMARY KEY DEFAULT gen_random_uuid()::TEXT,
  household_id UUID        NOT NULL REFERENCES households(id) ON DELETE CASCADE,
  child_id     TEXT        NOT NULL REFERENCES children(id)   ON DELETE CASCADE,
  summary_date DATE        NOT NULL,
  headline     TEXT        NOT NULL,
  summary      TEXT        NOT NULL,
  highlights   JSONB,
  generated_by TEXT        NOT NULL DEFAULT 'ai',
  created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (child_id, summary_date)
);

CREATE OR REPLACE TRIGGER set_daily_summaries_updated_at
  BEFORE UPDATE ON daily_summaries
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

CREATE INDEX IF NOT EXISTS idx_daily_summaries_child_date
  ON daily_summaries (child_id, summary_date DESC);

CREATE INDEX IF NOT EXISTS idx_daily_summaries_household
  ON daily_summaries (household_id);

-- RLS
ALTER TABLE daily_summaries ENABLE ROW LEVEL SECURITY;

-- Both roles read their household's summaries
CREATE POLICY "daily_summaries:select" ON daily_summaries FOR SELECT
  USING (household_id = my_household_id());

-- Summaries are written server-side (service role bypasses RLS).
-- This policy allows parent clients to write if needed (e.g. manual entry).
CREATE POLICY "daily_summaries:insert" ON daily_summaries FOR INSERT
  WITH CHECK (household_id = my_household_id() AND my_role() = 'parent');

CREATE POLICY "daily_summaries:update" ON daily_summaries FOR UPDATE
  USING (household_id = my_household_id() AND my_role() = 'parent');

CREATE POLICY "daily_summaries:delete" ON daily_summaries FOR DELETE
  USING (household_id = my_household_id() AND my_role() = 'parent');


-- ============================================================
-- 4. sprout_notices — new table
-- ============================================================
-- AI-generated or system notices scoped to a household + child.
-- dismissed_by is a JSONB object: { "nanny": true, "parent": false }
-- A notice is fully dismissed once both roles have dismissed it
-- (or when appropriate for the notice_type).

CREATE TABLE IF NOT EXISTS sprout_notices (
  id           TEXT        PRIMARY KEY DEFAULT gen_random_uuid()::TEXT,
  household_id UUID        NOT NULL REFERENCES households(id) ON DELETE CASCADE,
  child_id     TEXT        NOT NULL REFERENCES children(id)   ON DELETE CASCADE,
  notice_type  TEXT        NOT NULL,   -- 'developmental' | 'activity' | 'schedule' | 'milestone' | 'system'
  title        TEXT        NOT NULL,
  body         TEXT        NOT NULL,
  source       TEXT        NOT NULL DEFAULT 'ai',  -- 'ai' | 'system' | 'nanny' | 'parent'
  metadata     JSONB       NOT NULL DEFAULT '{}',
  dismissed_by JSONB       NOT NULL DEFAULT '{}',  -- { "nanny": true } once dismissed
  created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_sprout_notices_child
  ON sprout_notices (child_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_sprout_notices_household
  ON sprout_notices (household_id, created_at DESC);

-- RLS
ALTER TABLE sprout_notices ENABLE ROW LEVEL SECURITY;

-- Both roles read notices for their household
CREATE POLICY "sprout_notices:select" ON sprout_notices FOR SELECT
  USING (household_id = my_household_id());

-- Service role writes notices; both client roles can also create system notices
CREATE POLICY "sprout_notices:insert" ON sprout_notices FOR INSERT
  WITH CHECK (household_id = my_household_id());

-- Both roles can update dismissed_by (and only that field matters for updates)
CREATE POLICY "sprout_notices:update" ON sprout_notices FOR UPDATE
  USING (household_id = my_household_id());

-- Parents can hard-delete notices
CREATE POLICY "sprout_notices:delete" ON sprout_notices FOR DELETE
  USING (household_id = my_household_id() AND my_role() = 'parent');


