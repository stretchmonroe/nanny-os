CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;


-- ============================================================
-- 1. schedule_items — add missing beta columns
-- ============================================================
-- Existing columns kept as-is:
--   id, child_id, time (TEXT), title, type, done, active, notes,
--   scheduled_date, created_at
-- New columns are all nullable so existing rows are unaffected.

ALTER TABLE schedule_items
  ADD COLUMN IF NOT EXISTS household_id         UUID        REFERENCES households(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS description          TEXT,
  ADD COLUMN IF NOT EXISTS activity_type        TEXT,
  ADD COLUMN IF NOT EXISTS start_time           TIME,
  ADD COLUMN IF NOT EXISTS end_time             TIME,
  ADD COLUMN IF NOT EXISTS flexible_window_label TEXT,
  ADD COLUMN IF NOT EXISTS status               TEXT        CHECK (status IN ('planned','completed','skipped','replaced')) DEFAULT 'planned',
  ADD COLUMN IF NOT EXISTS created_by           TEXT,
  ADD COLUMN IF NOT EXISTS completed_by         TEXT,
  ADD COLUMN IF NOT EXISTS updated_at           TIMESTAMPTZ DEFAULT NOW();

-- Back-fill status from the legacy done boolean for existing rows
UPDATE schedule_items
  SET status = CASE WHEN done = TRUE THEN 'completed' ELSE 'planned' END
  WHERE status IS NULL;

-- Back-fill household_id from the child's household for existing rows
UPDATE schedule_items si
  SET household_id = c.household_id
  FROM children c
  WHERE si.child_id = c.id
    AND si.household_id IS NULL;

CREATE OR REPLACE TRIGGER set_schedule_items_updated_at
  BEFORE UPDATE ON schedule_items
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- Indexes
CREATE INDEX IF NOT EXISTS idx_schedule_items_child_date
  ON schedule_items (child_id, scheduled_date);

CREATE INDEX IF NOT EXISTS idx_schedule_items_household
  ON schedule_items (household_id);

CREATE INDEX IF NOT EXISTS idx_schedule_items_status
  ON schedule_items (child_id, status);


